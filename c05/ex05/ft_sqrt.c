int	ft_sqrt(int nb)
{
	int	i;

	i = 0;
	if (nb <= 0)
		return (0);
	while (i <= nb)
	{
		if (i * i == nb)
			return (i);
		if (i && i > nb / i)
			return (0);
		i++;
	}
	return (0);
}
