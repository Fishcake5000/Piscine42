int	ft_plus(int n1, int n2)
{
	return (n1 + n2);
}

int	ft_minus(int n1, int n2)
{
	return (n1 - n2);
}

int	ft_times(int n1, int n2)
{
	return (n1 * n2);
}

int	ft_divide(int n1, int n2)
{
	return (n1 / n2);
}

int	ft_modulo(int n1, int n2)
{
	return (n1 % n2);
}
